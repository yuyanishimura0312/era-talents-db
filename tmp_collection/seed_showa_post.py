def names_for(domain):
    return [n.strip() for n in RAW[domain].replace("\n", ",").split(",") if n.strip()]


def wiki_url(name):
    return "https://ja.wikipedia.org/wiki/" + urllib.parse.quote(name)


def main():
    con = sqlite3.connect(DB)
    cur = con.cursor()
    rows = []
    seen = set()
    for domain in RAW:
        for name in names_for(domain):
            if name in seen:
                continue
            seen.add(name)
            great = 1 if name in CHECKLIST else 0
            local = 0 if great else 1
            rows.append((name, domain, great, local))
    if len(rows) < 680:
        raise SystemExit(f"Need 680 unique people, got {len(rows)}")

    inserted = 0
    target = 680
    offset = 0
    batch_no = 0
    while True:
        cur.execute("SELECT COUNT(*) FROM achievers WHERE primary_era_id='showa_post'")
        if cur.fetchone()[0] >= target:
            break
        batch = rows[offset:offset + 50]
        if not batch:
            raise SystemExit("Ran out of candidates before reaching target")
        offset += 50
        batch_no += 1
        con.execute("BEGIN")
        for name, domain, great, local in batch:
            cur.execute("SELECT COUNT(*) FROM achievers WHERE primary_era_id='showa_post'")
            if cur.fetchone()[0] >= target:
                break
            cur.execute("SELECT 1 FROM achievers WHERE name_ja=? AND birth_year IS NULL", (name,))
            if cur.fetchone():
                continue
