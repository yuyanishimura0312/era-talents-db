    seen = set()
    unique_candidates = []
    for row in CANDIDATES:
        if row[0] in EXCLUDE_NAMES:
            continue
        key = (row[0], row[1])
        if key in seen:
            continue
        seen.add(key)
        unique_candidates.append(row)

    conn = sqlite3.connect(DB_PATH)
    conn.execute("PRAGMA foreign_keys = ON")
    before = conn.execute(
        "SELECT COUNT(*) FROM achievers WHERE primary_era_id='heisei' AND domain LIKE '%business%'"
    ).fetchone()[0]
    total_inserted = 0
    used = []
    for start in range(0, len(unique_candidates), 50):
        if total_inserted >= 200:
            break
        batch = unique_candidates[start:start + 50]
        with conn:
            inserted = insert_batch(conn, batch)
        total_inserted += inserted
        used.extend(batch)
        current = conn.execute(
            "SELECT COUNT(*) FROM achievers WHERE primary_era_id='heisei' AND domain LIKE '%business%'"
        ).fetchone()[0]
        print(f"batch {start // 50 + 1}: inserted={inserted}, heisei_business_count={current}")

    # 候補スキップで200件に届かない場合は明示的に失敗させる。
    if total_inserted < 200:
        raise SystemExit(f"inserted only {total_inserted}; add more candidates")

    # 200件を超えた場合は今回チームの末尾から余剰を削る。
    if total_inserted > 200:
        extra = total_inserted - 200
        ids = [
            row[0]
            for row in conn.execute(
                "SELECT id FROM achievers WHERE source_team=? ORDER BY id DESC LIMIT ?",
                (TEAM, extra),
            ).fetchall()
        ]
        with conn:
            conn.executemany("DELETE FROM achiever_capabilities WHERE achiever_id=?", [(i,) for i in ids])
            conn.executemany("DELETE FROM achievers WHERE id=?", [(i,) for i in ids])
        total_inserted = 200

    after = conn.execute(
        "SELECT COUNT(*) FROM achievers WHERE primary_era_id='heisei' AND domain LIKE '%business%'"
    ).fetchone()[0]
    caps = conn.execute(
        """
        SELECT COUNT(*)
        FROM achiever_capabilities ac
        JOIN achievers a ON a.id = ac.achiever_id
        WHERE a.source_team=?
        """,
        (TEAM,),
    ).fetchone()[0]
    great = conn.execute(
        "SELECT COALESCE(SUM(is_traditional_great),0), COUNT(*) FROM achievers WHERE source_team=?",
        (TEAM,),
    ).fetchone()
    local = conn.execute(
        "SELECT COALESCE(SUM(is_local_excellent),0) FROM achievers WHERE source_team=?",
        (TEAM,),
    ).fetchone()[0]
    print(f"before={before}, after={after}, inserted={after - before}")
    print(f"team_rows={great[1]}, traditional_great={great[0]}, local_excellent={local}, capabilities={caps}")
    conn.close()


