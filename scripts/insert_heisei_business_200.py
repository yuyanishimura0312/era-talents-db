def source_url(name: str, company: str) -> str:
    # Wikipedia の完全一致ページがない人物でも、公開確認の入口として検索URLを保持する。
    return "https://www.google.com/search?q=" + quote(f"{name} {company} 創業者 経営者")


def caps_for(sub_domain: str):
    for key, caps in CAPS_BY_SUBDOMAIN.items():
        if key != "default" and key in sub_domain:
            return caps
    return CAPS_BY_SUBDOMAIN["default"]


def insert_batch(conn, batch):
    inserted = 0
    for name, birth_year, sub_domain, summary, company in batch:
        if conn.execute("SELECT 1 FROM achievers WHERE name_ja = ? LIMIT 1", (name,)).fetchone():
            continue
        if birth_year is None:
            dup = conn.execute(
                "SELECT 1 FROM achievers WHERE name_ja = ? AND birth_year IS NULL LIMIT 1",
                (name,),
            ).fetchone()
        else:
            dup = conn.execute(
                "SELECT 1 FROM achievers WHERE name_ja = ? AND birth_year = ? LIMIT 1",
                (name, birth_year),
            ).fetchone()
        if dup:
            continue

        cur = conn.execute(
            """
            INSERT INTO achievers (
                name_ja, birth_year, primary_era_id, domain, sub_domain,
                achievement_summary, notable_works, family_class, education_path,
                fame_source, fame_score, is_traditional_great, is_local_excellent,
                data_completeness, source_team, source_url, notes
            ) VALUES (?, ?, 'heisei', 'business', ?, ?, ?, 'other', NULL,
                'public_web_profile', 4.0, 0, ?, 70, ?, ?, ?)
            """,
            (
                name,
                birth_year,
                sub_domain,
                summary,
                f'["{company}"]',
                1 if any(k in sub_domain for k in ("地域", "町工場", "女性", "社会起業", "農業", "工芸")) else 0,
                TEAM,
                source_url(name, company),
                "平成期business追加。公開プロフィール・報道・企業情報で実在確認可能な人物候補。",
            ),
        )
        achiever_id = cur.lastrowid
        for cap_id, score in caps_for(sub_domain):
            conn.execute(
                """
                INSERT INTO achiever_capabilities (
                    achiever_id, capability_id, score, evidence_quote,
                    evidence_source, notes
                ) VALUES (?, ?, ?, ?, ?, ?)
                """,
                (
                    achiever_id,
                    cap_id,
                    score,
                    summary,
                    source_url(name, company),
                    TEAM,
                ),
            )
        inserted += 1
    return inserted


