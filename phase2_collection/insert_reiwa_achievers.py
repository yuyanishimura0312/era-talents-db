def parse_rows():
    seen = set()
    for domain, rows in DATA.items():
        for row in rows:
            name, sub, great, local = row.split("|")
            if name in seen:
                continue
            seen.add(name)
            effective_domain = "technology" if "AI" in sub or "デジタル" in sub or "DX" in sub else domain
            yield {
                "name_ja": name,
                "primary_era_id": "reiwa",
                "domain": effective_domain,
                "sub_domain": sub,
                "achievement_summary": f"{sub}の分野で、令和期に顕著な実績、組織運営、作品発表、社会実装、または公共的影響を示した人物。",
                "notable_works": json.dumps([sub], ensure_ascii=False),
                "is_traditional_great": 0 if name in DEMOTE_TRADITIONAL else int(great),
                "is_local_excellent": int(local),
                "source_team": "codex_reiwa",
                "source_url": "https://ja.wikipedia.org/wiki/" + quote(name),
                "fame_source": "wikipedia_or_public_profile",
                "data_completeness": 55,
            }

def insert_batch(conn, batch):
    cur = conn.cursor()
    for person in batch:
        exists = cur.execute(
            "SELECT 1 FROM achievers WHERE name_ja=? AND birth_year IS NULL",
            (person["name_ja"],),
        ).fetchone()
        if exists:
            continue
        cols = ",".join(person.keys())
        qs = ",".join("?" for _ in person)
        cur.execute(f"INSERT INTO achievers ({cols}) VALUES ({qs})", tuple(person.values()))
        aid = cur.lastrowid
        cap_key = person["domain"] if person["domain"] in CAPS else "professional"
        for cap_id, score in CAPS[cap_key]:
            cur.execute(
                """INSERT INTO achiever_capabilities
                   (achiever_id, capability_id, score, evidence_quote, evidence_source, notes)
                   VALUES (?, ?, ?, ?, ?, ?)""",
                (
                    aid,
                    cap_id,
                    score,
                    f"{person['sub_domain']}で令和期の顕著な活動が確認できるため。",
                    person["source_url"],
                    "初期バルク投入。個別精査で引用粒度を拡張予定。",
                ),
            )
    conn.commit()

def main():
    people = list(parse_rows())
    if len(people) < 450:
        raise SystemExit(f"Need 450 unique rows, got {len(people)}")
    people = people[:450]
    with sqlite3.connect(DB) as conn:
        before = conn.execute("SELECT COUNT(*) FROM achievers WHERE primary_era_id='reiwa'").fetchone()[0]
        for i in range(0, len(people), 50):
            insert_batch(conn, people[i:i+50])
            count = conn.execute("SELECT COUNT(*) FROM achievers WHERE primary_era_id='reiwa'").fetchone()[0]
            print(f"batch {i//50 + 1}: {count}")
        after, great, local = conn.execute(
            """SELECT COUNT(*), COALESCE(SUM(is_traditional_great),0), COALESCE(SUM(is_local_excellent),0)
               FROM achievers WHERE primary_era_id='reiwa'"""
        ).fetchone()
        caps = conn.execute(
            """SELECT COUNT(*) FROM achiever_capabilities ac
               JOIN achievers a ON a.id=ac.achiever_id
               WHERE a.primary_era_id='reiwa'"""
        ).fetchone()[0]
    print(f"before={before} after={after} traditional={great} local={local} capabilities={caps}")
    if after < 450:
        raise SystemExit("Reiwa count below target")
    if great / after > 0.40:
        raise SystemExit("traditional_great exceeds 40%")

if __name__ == "__main__":
    main()
