def main() -> None:
    data = list(rows())
    assert len(data) == 410
    inserted = 0
    skipped = 0
    conn = sqlite3.connect(DB_PATH)
    try:
        for i, row in enumerate(data, 1):
            if (i - 1) % 50 == 0:
                conn.execute("BEGIN")
            if row["birth_year"] is None:
                exists = conn.execute(
                    "SELECT 1 FROM achievers WHERE name_ja=? AND birth_year IS NULL",
                    (row["name_ja"],),
                ).fetchone()
            else:
                exists = conn.execute(
                    "SELECT 1 FROM achievers WHERE name_ja=? AND birth_year=?",
                    (row["name_ja"], row["birth_year"]),
                ).fetchone()
            if exists:
                skipped += 1
            else:
                cur = conn.execute(
                    """
                    INSERT INTO achievers (
                        name_ja, birth_year, death_year, primary_era_id, domain, sub_domain,
                        achievement_summary, notable_works, is_traditional_great,
                        is_local_excellent, data_completeness, source_team, source_url
                    ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """,
                    (
                        row["name_ja"], row["birth_year"], row["death_year"], ERA, row["domain"],
                        row["sub_domain"], row["summary"], row["notable_works"],
                        row["is_traditional_great"], row["is_local_excellent"], 70,
                        TEAM, row["source_url"],
                    ),
                )
                achiever_id = cur.lastrowid
                for cap_id, score in DOMAIN_CAPS[row["domain"]]:
                    conn.execute(
                        """
                        INSERT INTO achiever_capabilities (
                            achiever_id, capability_id, score, evidence_quote, evidence_source, notes
                        ) VALUES (?, ?, ?, ?, ?, ?)
                        """,
                        (
                            achiever_id, cap_id, score,
                            f"{row['summary']} 能力評価は人物の主要活動領域に基づく。",
                            row["source_url"],
                            "batch_seed_showa_pre",
                        ),
                    )
                inserted += 1
            if i % 50 == 0 or i == len(data):
                conn.commit()
                count = conn.execute(
                    "SELECT COUNT(*) FROM achievers WHERE primary_era_id=?",
                    (ERA,),
                ).fetchone()[0]
                print(f"batch_end={i} inserted={inserted} skipped={skipped} showa_pre_count={count}")
        great, total = conn.execute(
            "SELECT SUM(is_traditional_great), COUNT(*) FROM achievers WHERE primary_era_id=?",
            (ERA,),
        ).fetchone()
        caps = conn.execute(
            """
            SELECT COUNT(*) FROM achiever_capabilities
            WHERE achiever_id IN (SELECT id FROM achievers WHERE primary_era_id=?)
            """,
            (ERA,),
        ).fetchone()[0]
        print(f"final_total={total} traditional_great={great or 0} ratio={(great or 0)/total:.3f} capabilities={caps}")
    finally:
        conn.close()


if __name__ == "__main__":
    main()
