def insert_batch(conn: sqlite3.Connection, rows):
    for row in rows:
        (
            name_ja,
            name_en,
            name_kana,
            birth_year,
            death_year,
            birth_place,
            sub_domain,
            summary,
            works,
            family_class,
            family_education,
            education_path,
            mentors,
            great,
        ) = row
        duplicate = conn.execute(
            "SELECT 1 FROM achievers WHERE name_ja=? AND birth_year=?",
            (name_ja, birth_year),
        ).fetchone()
        if duplicate:
            raise RuntimeError(f"duplicate achiever: {name_ja} {birth_year}")

        cur = conn.execute(
            """
            INSERT INTO achievers (
                name_ja, name_en, name_kana, birth_year, death_year, birth_place,
                primary_era_id, secondary_era_id, domain, sub_domain,
                achievement_summary, notable_works, family_class, family_education,
                education_path, mentors, fame_source, fame_score,
                is_traditional_great, is_local_excellent, data_completeness,
                source_team, source_url, notes
            ) VALUES (?, ?, ?, ?, ?, ?, 'meiji', NULL, 'science_tech', ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                name_ja,
                name_en,
                name_kana,
                birth_year,
                death_year,
                birth_place,
                sub_domain,
                summary,
                json.dumps(works, ensure_ascii=False),
                family_class,
                family_education,
                education_path,
                json.dumps(mentors, ensure_ascii=False),
                "wikipedia_ja_or_ndl_verifiable",
                55.0 if great else 35.0,
                great,
                0 if great else 1,
                82,
                SOURCE_TEAM,
                wiki_url(name_ja),
                "明治期science_tech追加。Wikipedia/NDL等で実在確認可能な人物に限定。",
            ),
        )
        achiever_id = cur.lastrowid
        for capability_id, score in capability_profile(sub_domain):
            conn.execute(
                """
                INSERT INTO achiever_capabilities (
                    achiever_id, capability_id, score, evidence_quote, evidence_source, notes
                ) VALUES (?, ?, ?, ?, ?, ?)
                """,
                (
                    achiever_id,
                    capability_id,
                    score,
                    f"DB作成者要約: {summary}",
                    wiki_url(name_ja),
                    SOURCE_TEAM,
                ),
            )


